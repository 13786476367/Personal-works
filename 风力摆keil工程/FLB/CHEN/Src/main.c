void SetPwm(short moto1,short moto2)
{
	    if(moto1>=0)
		{
			TIM3->CCR1 = moto1;
			TIM3->CCR3 = 0;
		}
		else
		{
			TIM3->CCR3 = -moto1;
			TIM3->CCR1 = 0;
		}
		if(moto2>=0)
		{
			TIM3->CCR4 = moto2;
			TIM3->CCR2 = 0;
		}
		else 
		{
			TIM3->CCR2 = -moto2;
			TIM3->CCR4 = 0;
		}
}

